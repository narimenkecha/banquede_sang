<h1 class="title3">Masques / Revêtements Faciaux Requis Dans Tous Les Centres Et Lecteurs Pulse De La vie</h1>
<p  class="definition1">
Pulse De  La vie continue d'exiger de tous les donneurs qu'ils portent des masques ou des masques raisonnables à base de tissu lors des collectes de sang et de nos centres de donneurs. Ceci afin de vous protéger, vous, vos collègues donateurs, ainsi que les membres du personnel de Pulse De  La vie.
 Notre personnel chargé des collections continuera également à porter des masques.
 <br>
 <strong>Remarque: les masques munis de valves n'empêchent pas la propagation du COVID-19. </strong>
</p>
<h1 class="title3">
Avis Aux Donneurs - NE PAS Donner De Sang SI:</h1>

<p  class="definition1">
-Vous avez eu une nouvelle toux, un essoufflement, de la fièvre, des frissons, des douleurs musculaires, une nouvelle perte de votre goût ou de votre odorat, des maux de tête, des maux de gorge, une fatigue extrême ou d'autres symptômes inexpliqués au cours des 14 derniers jours.
<br>
-Vous vivez ou avez été en contact étroit avec une personne diagnostiquée ou soupçonnée d'avoir le COVID-19 au cours des 14 derniers jours.
<br>
-Vous avez été diagnostiqué ou soupçonné d'avoir le COVID-19 et vous avez présenté des symptômes au cours des 28 derniers jours.
<br>
-Vous êtes un professionnel de la santé qui a directement soigné des patients hospitalisés atteints d'une infection active au COVID-19 au cours des 28 derniers jours.
<br>
-Vous souhaitez vous faire tester pour COVID-19. Les banques de sang ne testent PAS une infection active au COVID-19.
</p>
<h1 class="title3">Mesures Que Nous Prenons:</h1>
<p  class="definition1">
-Nous encourageons fortement la distanciation sociale et avons mis en place des procédures pour imposer une distance minimale de six pieds entre les donateurs chaque fois que possible.
<br>
-Seules les personnes en bonne santé sont encouragées à donner du sang. En fait, les donneurs doivent se sentir bien et ne pas présenter de symptômes de maladie, y compris de fièvre, pour être éligibles à un don.
<br>
-Tous les donneurs de sang potentiels sont examinés «à la porte» pour les symptômes liés au COVID-19, et tous les donneurs sont contrôlés pour s'assurer que leur température est normale AVANT d'entrer dans une zone de prélèvement.
<br>
-Les personnes qui répondent à certains critères à risque pour le COVID-19 sont différées à l'avance du don.
<br>
-Nous limitons le nombre de personnes autorisées dans les centres de donateurs et les lecteurs mobiles. Nous pouvons vous demander de laisser un numéro de téléphone portable et d'attendre dans votre voiture jusqu'à ce que votre tour soit appelé.
<br>
-Nous séparons les chaises de 6 pieds dans les zones d'attente et de don, selon les conseils du CDC.
<br>
-Notre personnel porte des gants et un équipement de protection individuelle et essuie souvent les zones touchées par les donneurs et après chaque prélèvement à l'aide d'un désinfectant.
<br>
-Le personnel utilise des ensembles de prélèvement stériles à usage unique pour chaque donneur et frotte le bras du donneur pendant 30 secondes avant le prélèvement.
Notre personnel est dépisté tous les jours pour les symptômes liés au COVID-19 et la température vérifiée avant de commencer à travailler, et sont tenus de quitter le travail s'ils ressentent des symptômes pendant la journée.
<br>
-Le personnel qui ne se sent pas bien doit rester à la maison.

</p>
<h1 class="title3">
Mesures Que Vous Pouvez Prendre</h1>
<p  class="definition1">
<strong>Actions que vous pouvez prendre pour assurer votre sécurité, selon le CDC:</strong>
-Portez un masque lorsque vous êtes en public. Assurez-vous que le masque couvre le nez et la bouche.
<br>
-Lavez-vous fréquemment les mains avec du savon et de l'eau ou un désinfectant pour les mains à base d'alcool et évitez le contact mains à visage, principal mécanisme de transmission.
<br>
-Nettoyer les surfaces de travail conformément aux procédures de contrôle des infections établies.
<br>
-Pratiquez la distance sociale - en restant à au moins 1,80 mètre - des autres personnes.
<br>
-Auto-quarantaine après avoir pris soin d'une personne malade.
<br>
-Restez à la maison lorsque vous ressentez des symptômes de maladie.
<br>
-Utilisez une bonne étiquette contre la toux; couvrir la bouche et le nez lorsque vous toussez ou éternuez et évitez de toucher les yeux, le nez ou la bouche. 

</p>













