<p class="definition1">
  Voici quelques moyens rapides d'aider votre don à réussir:
  <h1 class="title3">Mange!</h1>
 
<p  class="definition1">
 <strong>1.Fer et vitamine C:</strong> combinaison d'aliments riches en fer et en vitamine C augmentera la force de votre corps - force que vous pouvez donner à un patient qui en a besoin! Pensez aux légumes verts et aux agrumes.
  </p>
<p class="definition1">
  <strong>2.Collations salées:</strong> bonne quantité de collations salées peut contribuer à faire de votre prochain don de sang une expérience positive et à garantir que votre don sera utilisé pour sauver une ou plusieurs vies!
</p>
<p class="definition1">
 <strong>3.Mais ne sautez pas de repas:</strong> Considérez le don de sang comme une partie normale de votre journée. Ne sautez pas et n’ajoutez pas de repas à votre routine habituelle. Ajoutez simplement quelques collations saines.
  </p>
<h1 class="title3">Boire!</h1>
<p class="definition1">
  L'hydratation est importante avant et après votre don. Lorsque vous donnez du sang, vous donnez une pinte de sang à la fois et presque la moitié est de l'eau. Plus vous buvez d'eau, plus votre sang circule facilement dans votre corps et plus vite vous pouvez remplacer la pinte que vous avez donnée!
  </p>
<h1 class="title3">Bouger!</h1>
<p class="definition1">
  Dans les moments qui précèdent et pendant votre don, faites quelques exercices simples de flexion ou de tension. Commencez par les orteils et progressez en tendant les muscles des bras, des jambes ou du tronc pendant quelques secondes, puis détendez-vous. Ces mouvements simples garderont votre sang en mouvement pour un don relaxant!
</p>
<h1 class="title3">Pendant Votre Don</h1>
<p class="definition1">
Offrir le don de la vie grâce au don de sang est facile! Voici les quatre étapes faciles pour devenir un héros à sang rouge:
</p>

<p class="definition1">
 <strong>1.Inscription, dépistage et mini-physique:</strong> Connectez-vous avec notre sympathique réceptionniste pour remplir un bref questionnaire sur vos antécédents de santé et nos membres du personnel de banque de sang agréés vous donneront un mini-physique qui vérifiera votre tension artérielle, votre pouls, votre température et vos niveaux de fer.
</p>


<p class="definition1">
 <strong>2.Don:</strong> Passons à la raison principale pour laquelle vous êtes ici - votre don vital! Votre sang est prélevé par un professionnel de la santé qualifié à l'aide de matériaux neufs, stériles et jetables.

</p>

<p class="definition1">
  <strong>3.Ressourcez-vous à la cantine:</strong>Toutes nos félicitations! Vous avez fait une différence vitale dans la vie des patients dans le besoin. Soyez fier de votre acte héroïque! Rendez-vous maintenant à la cantine et redonnez de l'énergie à des collations et des boissons saines (mais délicieuses).
</p>
<h1 class="title3">Après Votre Don</h1>
<p class="definition1">
  Une fois votre don terminé, le voyage de votre sang ne fait que commencer!
  <br>
  <br>
  Pour protéger les patients susceptibles de recevoir votre sang, il passera par un certain nombre de tests pour plusieurs types d'infections, des anticorps potentiellement nocifs et, bien sûr, pour confirmer votre groupe sanguin.
  <br>
  <br>
 Votre sang sera ensuite séparé en composants si nécessaire, emballé et envoyé à un hôpital local où il pourrait simplement sauver la vie de quelqu'un!
</p>
</p>

