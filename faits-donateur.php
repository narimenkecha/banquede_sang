<h1 class="title4">Faits Sur Le Donateurs</h1></div>
<p class="definition1">Ceux qui donnent du sang aident tout le monde. Nous sommes là pour répondre à toutes vos questions sur le processus de don de sang.
<h1 class="title3">1.Pourquoi devrais-je donner du sang?</h1>
<p class="definition1">
  Le sang est nécessaire quotidiennement pour aider les patients dans les hôpitaux locaux et ne peut pas être fabriqué. Votre don pourrait être utilisé pour des traitements contre le cancer, des chirurgies, des greffes d'organes, des nouveau-nés, des brûlures graves et des traumatismes. Chaque fois que vous donnez, vous sauvez des vies!

</p>
<h1 class="title3">2.Comment vais-je me sentir après le don?</h1>
<p class="definition1">
  La plupart des gens se sentent bien en sachant qu'ils ont contribué à sauver une vie! Quelques-uns peuvent se sentir lents ou faibles immédiatement après le don.

</p>
<h1 class="title3">3.Le don de sang est-il sûr? Est-ce que ça fait mal?</h1>
<p class="definition1">
  Non seulement il est sûr, il est simple et il sauve des vies. Vous ne pouvez pas contracter le SIDA ou toute autre maladie en faisant un don de sang. Le matériel utilisé lors de votre don est neuf, stérile et jeté après chaque don. Au moment du piqûre d'aiguille, vous ressentirez une pincée mineure sur le site.

</p>
<h1 class="title3">4.Quelle est la durée du processus de don?</h1>
<p class="definition1">
  Un don de sang total lui-même prend environ 6 à 8 minutes. L'ensemble du processus de don, de l'inscription aux rafraîchissements après le don, prend environ une heure.
  
</p>

<h1 class="title3">5.Dois-je apporter quelque chose avec moi pour faire un don?</h1>
<p class="definition1">
  Tout ce dont vous avez besoin est une pièce d'identité avec photo avec preuve d'âge, comme votre permis de conduire, une attitude positive et un cœur généreux. Si vous avez 15 ou 16 ans, veuillez apporter un formulaire de consentement signé banque de Sang Young Donor.
</p>

<h1 class="title3">6.À quelle fréquence puis-je faire un don ?  </h1>
<p class="definition1">
  Vous pouvez donner du sang total en toute sécurité tous les 56 jours. Les donneurs de plasma peuvent donner une fois par mois et les donneurs de plaquettes peuvent donner toutes les deux semaines.
</p>


</p>
