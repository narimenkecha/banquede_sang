<h1 class="title4">Questions Communes Et Restrictions Sur Les Dons</h1></div>
  <h1 class="title3"> -Age</h1>
  <p class="definition1"> 
    Il n'y a pas d'âge maximum pour donner du sang. L'âge minimum pour donner du sang total est de 15 ans.
     L'âge minimum pour donner des plaquettes et du plasma est de 17 ans.
  </p>

<h1 class="title3">-Antibiotiques</h1>
<p class="definition1">
    Vous pouvez donner du sang si vous avez terminé la dose prescrite d'antibiotiques et que vous ne présentez plus aucun symptôme de la raison pour laquelle ils ont été prescrits.
</p>
<h1 class="title3">-Pression artérielle</h1>

<p class="definition1">
    Avant le don, chaque donneur potentiel fait mesurer sa tension artérielle pour s'assurer que les lectures respectent les directives de don. Les niveaux «extrêmes» sont 90/50 et 180/100. En dessous du premier ou au-dessus du second, le report du donneur potentiel sera reporté. Et vous obtenez toujours un chèque gratuit pour être sûr!
</p> 
<h1 class="title3">-Cancer</h1>
<p class="definition1">
-Si vous souffrez actuellement d'une forme de cancer, vous ne pouvez pas faire de don
<br>
<br>
-Si vous avez un cancer de la peau non mélanome (cancer basocellulaire ou épidermoïde de la peau), vous pouvez faire un don dès que vous êtes guéri de l'élimination complète de la zone concernée
<br><br>
-Pour le cancer de la peau avec mélanome, vous devez attendre 1 an à compter de la fin du traitement.
<br><br>
-Si vous avez déjà eu une leucémie ou un lymphome, vous ne pouvez pas faire de don (sauf pour les personnes atteintes d'une leucémie infantile traitée avec succès).
<br><br>
-Tous les autres types de cancer sont acceptables SI votre médecin vous a déclaré «sans cancer» et que votre traitement (comme la radiothérapie et / ou la chimiothérapie) est terminé. Remarque: Les femmes atteintes d'un cancer du sein qui prennent des médicaments bloquant les hormones sont éligibles, et les donneuses non cancéreuses mais qui ont des implants radioactifs sont également éligibles.
</p>
<h1 class="title3">-Diabète</h1>
<p class="definition1">
    Nous conseillons aux donneurs potentiels diabétiques de bien contrôler leur alimentation et / ou leurs médicaments, sans épisodes récents d'hyperglycémie ou d'hypoglycémie importants. Sinon, les donneurs diabétiques ont le droit de donner du sang.
</p>
<h1 class="title3">-Problèmes cardiaques et pulmonaires</h1>
<p class="definition1">
-Souffles cardiaques ou battements cardiaques irréguliers: éligibles tant que vous n'avez pas de limites à votre activité de la part de votre fournisseur et que vous n'avez aucun problème avec les activités.
<br><br>
-Pacemaker: éligible tant que vous n'avez pas de limitations sur votre activité de votre fournisseur et que vous n'avez aucun problème avec les activités.
<br><br>
-Crise cardiaque, angine de poitrine: éligible 6 mois après votre dernier événement, tant que votre médecin vous a donné la permission verbale de faire un don (aucune note requise), vous n'avez aucun problème avec les activités normales et vous n'avez aucune restriction ou limitation de la part de votre fournisseur.
<br><br>
-Chirurgie cardiaque (pontage coronarien, réparation valvulaire, pose de stent): éligible 6 mois après la procédure, tant que votre médecin vous a donné la permission verbale de faire un don (aucune note requise), vous n'avez aucun problème avec les activités normales et vous avez aucune restriction ou limitation de votre fournisseur.
<br><br>
-Sténose aortique, modérée à sévère (non réparée): Vous ne pouvez pas faire de don.
<br><br>
-Asthme: éligible, tant que vous respirez bien aujourd'hui.
</p>
<h1 class="title3">-Hypertension artérielle</h1>
<p class="definition1">
    Si vous souffrez d'hypertension artérielle, vous pouvez donner du sang tant que votre lecture sur le site du don ne dépasse pas 180/100. Nous prendrons votre tension artérielle juste avant de faire un don, vous recevrez donc un chèque gratuit à coup sûr!
</p>
<h1 class="title3">-Faible teneur en fer ou anémie</h1>
<p class="definition1">
    Certains donneurs sont plus à risque d'avoir des réserves de fer corporel épuisées, notamment des donneuses de moins de 50 ans, des donneuses adolescentes des deux sexes et des donneurs de sang fréquents des deux sexes. Nous recommandons un régime riche en aliments contenant du fer, mais les donneurs à risque devraient envisager une supplémentation en fer à faible dose pris par voie orale (discutez d'abord avec votre professionnel de la santé, cependant). Plus d'informations sont disponibles sur demande
    </p>
<h1 class="title3">-Médicaments</h1>
<p class="definition1">
    Les médicaments pris pour la tension artérielle, le cholestérol et le contrôle des naissances sont tous acceptables pour donner du sang. Certains médicaments contre l'acné et les médicaments contre la prostate sont une cause de report.

</p>
<h1 class="title3">-Grossesse</h1>
<p class="definition1">
    À de rares exceptions près, si vous êtes enceinte, vous ne devez pas donner de sang avant 6 semaines après l'accouchement. Après 6 semaines, vous pouvez reprendre le don, même si vous allaitez (nous vous conseillons toutefois d'envisager une conversation avec votre médecin avant de le faire).

</p>
<h1 class="title3">-Accident vasculaire cérébral</h1>
<p class="definition1">
    -AVC majeur: attendre 6 mois; éligible uniquement s'il est entièrement récupéré sans effets résiduels.
    <br>
    -Attaque ischémique transitoire (AIT, «mini-AVC»): attendre 6 mois; éligible uniquement s'il est entièrement récupéré sans effets résiduels.
</p>

